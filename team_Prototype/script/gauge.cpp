//====================================================================================================
// マクロ定義
//====================================================================================================
#define BULLET_SIZE (40)
#define MAX_LIFE (40)

//====================================================================================================
// プロトタイプ宣言
//====================================================================================================

//====================================================================================================
// グローバル変数
//====================================================================================================
LPDIRECT3DTEXTURE9 CGauge::m_pTextureGauge[MAX_TEXGAUGE] = {};

//====================================================================================================
// コンストラクタ
//====================================================================================================
CGauge::CGauge(OBJTYPE type) : CScene2D(type)
{
	m_pos = D3DXVECTOR3(0.0f, 0.0f, 0.0f);
	m_nLife = 0;
}

CGauge::CGauge()
{
}

//=============================================================================
// 初期化処理
//=============================================================================
HRESULT CGauge::Init(D3DXVECTOR3 pos)
{
	CScene2D::Init();

	SetPos(pos);

	return S_OK;
}

//==================================================================================================
//終了処理
//===================================================================================================
void CGauge::Uninit(void)
{
	CScene2D::Uninit();
}

//====================================================================================================
//更新処理
//=====================================================================================================
void CGauge::Update(void)
{
	//変数宣言
	int nLife = 5;/*pPlayer->GetLife();*/

	SetColor(D3DXCOLOR(0.0f, 1.0f, 0.0f, 1.0f));

	CScene2D::UIUpdate(nLife - 1, 100);
}

//========================================================================================================
// 移動処理
//========================================================================================================
void CGauge::SetLife(int nLife)
{
	m_nLife = nLife;
}


//========================================================================================================
// 体力の取得
//========================================================================================================
int CGauge::GetLife()
{
	return m_nLife;
}

//========================================================================================================
// 描画処理
//========================================================================================================
CGauge *CGauge::Create(D3DXVECTOR3 pos)
{
	CGauge *pBullet;

	pBullet = new CGauge(OBJTYPE_RANKING);

	pBullet->BindTexture(m_pTextureGauge[0]);

	pBullet->Init(pos);

	return pBullet;
}

//========================================================================================================
// テクスチャの読み込み
//========================================================================================================
HRESULT CGauge::Load(void)
{
	CRenderer *pRenderer = CManager::GetRenderer();
	LPDIRECT3DDEVICE9 pDevice = pRenderer->GetDevice();

	//テクスチャの読み込み
	D3DXCreateTextureFromFile(
		pDevice,
		TEXTUER_017,
		&m_pTextureGauge[0]);

	return S_OK;
}
//=======================================================================================================
// テクスチャの破棄
//========================================================================================================
void CGauge::Unload(void)
{
	for (int nCnt =0; nCnt < MAX_TEXGAUGE; nCnt++)
	{
		// 頂点バッファの開放
		if (m_pTextureGauge[nCnt] != NULL)
		{
			m_pTextureGauge[nCnt]->Release();
			m_pTextureGauge[nCnt] = NULL;
		}
	}
}

//========================================================================================================
// 描画処理
//========================================================================================================
void CGauge::Draw(void)
{
	CScene2D::Draw();
}

