//====================================================================================================
// グローバル変数
//====================================================================================================
LPDIRECT3DTEXTURE9 CGauge::m_pTextureGauge[MAX_TEXGAUGE] = {};

//====================================================================================================
// コンストラクタ
//====================================================================================================
CGauge::CGauge(OBJTYPE type) : CScene2D(type)
{
	m_pos = D3DXVECTOR3(0.0f, 0.0f, 0.0f);
	m_nLife = 0;
}

CGauge::CGauge()
{
}

//=============================================================================
// 初期化処理
//=============================================================================
HRESULT CGauge::Init(D3DXVECTOR3 pos,int Life, CPlayerBase::PLAYERTYPE playerType)
{
	CScene2D::Init();

	m_nLife = Life;

	PlayerType = playerType;

	SetPos(pos);
	SetColor(D3DXCOLOR(0.0f, 1.0f, 0.0f, 1.0f));

	return S_OK;
}

//==================================================================================================
//終了処理
//===================================================================================================
void CGauge::Uninit(void)
{
	CScene2D::Uninit();
}

//====================================================================================================
//更新処理
//=====================================================================================================
void CGauge::Update(void)
{
	CScene2D::Update();

	for (int nCntModel = 0; nCntModel < MAX_POLYGON; nCntModel++)
	{
		CScene *pScene;

		pScene = CScene::GetScene(OBJTYPE_PLAYER, nCntModel);

		if (!pScene)
			continue;

		CPlayerBase *pPlayer = (CPlayerBase*)pScene;


		if (pPlayer->GetTypeChara() == PlayerType)
		{

			m_nLife = pPlayer->GetLife();
			break;
		}
	}

	if (m_nLife < 0)
	{
		m_nLife = 0;
	}

	UIUpdate(m_nLife, 45);
}

//========================================================================================================
// 描画処理
//========================================================================================================
CGauge *CGauge::Create(D3DXVECTOR3 pos, int Life, CPlayerBase::PLAYERTYPE playerType)
{
	CGauge *pGauge;

	pGauge = new CGauge(OBJTYPE_BG);

	pGauge->BindTexture(m_pTextureGauge[0]);

	pGauge->Init(pos, Life, playerType);

	return pGauge;
}

//========================================================================================================
// テクスチャの読み込み
//========================================================================================================
HRESULT CGauge::Load(void)
{
	CRenderer *pRenderer = CManager::GetRenderer();
	LPDIRECT3DDEVICE9 pDevice = pRenderer->GetDevice();

	//テクスチャの読み込み
	D3DXCreateTextureFromFile(
		pDevice,
		TEXTUER_017,
		&m_pTextureGauge[0]);

	return S_OK;
}
//=======================================================================================================
// テクスチャの破棄
//========================================================================================================
void CGauge::Unload(void)
{
	for (int nCnt =0; nCnt < MAX_TEXGAUGE; nCnt++)
	{
		// 頂点バッファの開放
		if (m_pTextureGauge[nCnt] != NULL)
		{
			m_pTextureGauge[nCnt]->Release();
			m_pTextureGauge[nCnt] = NULL;
		}
	}
}

//========================================================================================================
// 描画処理
//========================================================================================================
void CGauge::Draw(void)
{
	CScene2D::Draw();
}

